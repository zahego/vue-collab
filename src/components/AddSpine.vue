<template>
  <div>
      <h3>Add Spineyard</h3>
      <div class="add">
          <form @submit="onSubmit"> 
              <input type="text" v-model="titleInAddSpine" placeholder="Loading Spine..."/>
              <input type="submit" value="Submit"/>
          </form>
      </div>
  </div>
</template>

<script>
import {mapActions} from 'vuex';
export default {
    name: "AddSpine",
    data(){
        return{
            titleInAddSpine: ''
        }
    },
    methods: {
        ...mapActions(['addspineFromActions']),
        onSubmit(e){
            e.preventDefault();
            this.addspineFromActions(this.titleInAddSpine);
        }
    }
}
</script>

